// arithmetic operators % is modules+-/

#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter the first number\n");
    scanf ("%d", &a);
    printf("Enter the second number: \n");
    scanf("%d", &a);

    printf("a/b=%d\n", a/b);
    printf("a%%b=%d", a%b); //%is modulse and used for reminder
    return 0; 
}